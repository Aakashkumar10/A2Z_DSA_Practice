package Patterns_Star;

public class Pattern10 {
public static void  main(String args [])
{
	
	
	
for( int i =1;i<=5;i++)
{
	
	
for( int j=5;j<=i;j--)
{
	System.out.print(" ");
}
	
for( int k = 1;k<=2*i-1;k++)
{
	System.out.print("*");
}
	
System.out.println();
}
	
	
	
	
	
	
	
	
	
	
	
}
}
